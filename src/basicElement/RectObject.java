package basicElement;

/**
 * Store Four Parameter Class
 * 
 * @author ZEO_Lab
 * @version 1.2
 */
public class RectObject {
    
    /**
     * Four parameters
     */
    public int x, y,w, h;
    
    /**
     * Initialization
     * @param x parameter 1
     * @param y parameter 2
     * @param w parameter 3
     * @param h parameter 4
     */
    public RectObject(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }

}
